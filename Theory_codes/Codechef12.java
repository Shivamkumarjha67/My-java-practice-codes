
class Codechef12{
	public static void main (String[] args) throws java.lang.Exception
	{
		int arr[]= {1,2,3,4,5};
		printArray(arr);
	}
	static void printArray(int[] arr) {
        for(int i=0; i<arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
        System.out.println();
    }
}
